struct RootType {
    export_name: &'static str,
    schema: Schema,
}

fn main() -> anyhow::Result<()> {
    let repo_root = repo_root()?;
    let frontend_types = repo_root.join("frontend/src/api/generated.ts");
    let docs_schema_dir = repo_root.join("docs/schema");

    fs::create_dir_all(&docs_schema_dir)
        .with_context(|| format!("failed to create {}", docs_schema_dir.display()))?;

    let roots = vec![
        RootType { export_name: "TestType", schema: schema_for!(TestType) },
        RootType { export_name: "Protocol", schema: schema_for!(Protocol) },
        RootType { export_name: "HttpMethod", schema: schema_for!(HttpMethod) },
        RootType { export_name: "VlanProto", schema: schema_for!(VlanProto) },
        RootType { export_name: "NetworkMode", schema: schema_for!(NetworkMode) },
        RootType { export_name: "TestState", schema: schema_for!(TestState) },
        RootType { export_name: "TcpPayload", schema: schema_for!(TcpPayload) },
        RootType { export_name: "HttpPayload", schema: schema_for!(HttpPayload) },
        RootType { export_name: "PayloadProfile", schema: schema_for!(PayloadProfile) },
        RootType { export_name: "LoadConfig", schema: schema_for!(LoadConfig) },
        RootType { export_name: "Thresholds", schema: schema_for!(Thresholds) },
        RootType { export_name: "ClientDef", schema: schema_for!(ClientDef) },
        RootType { export_name: "ServerDef", schema: schema_for!(ServerDef) },
        RootType { export_name: "VlanConfig", schema: schema_for!(VlanConfig) },
        RootType { export_name: "Association", schema: schema_for!(Association) },
        RootType { export_name: "TcpOptions", schema: schema_for!(TcpOptions) },
        RootType { export_name: "TestConfig", schema: schema_for!(TestConfig) },
        RootType { export_name: "HistogramBucket", schema: schema_for!(HistogramBucket) },
        RootType { export_name: "PerProtocolSnapshot", schema: schema_for!(PerProtocolSnapshot) },
        RootType { export_name: "MetricsSnapshot", schema: schema_for!(MetricsSnapshot) },
        RootType { export_name: "RuntimeConfig", schema: schema_for!(RuntimeConfig) },
        RootType { export_name: "TestStatus", schema: schema_for!(TestStatus) },
        RootType { export_name: "TestResult", schema: schema_for!(TestResult) },
        RootType { export_name: "TestEventType", schema: schema_for!(TestEvent) },
    ];

    for root in &roots {
        let schema_path = docs_schema_dir.join(format!("{}.schema.json", root.export_name));
        let body = serde_json::to_string_pretty(&root.schema)?;
        fs::write(&schema_path, format!("{body}\n"))
            .with_context(|| format!("failed to write {}", schema_path.display()))?;
    }

    let generated = generate_typescript(&roots)?;
    fs::write(&frontend_types, generated)
        .with_context(|| format!("failed to write {}", frontend_types.display()))?;

    println!("generated {}", frontend_types.display());
    println!("generated {}", docs_schema_dir.display());

    Ok(())
}

fn repo_root() -> anyhow::Result<PathBuf> {
    let manifest_dir = Path::new(env!("CARGO_MANIFEST_DIR"));
    manifest_dir
        .ancestors()
        .nth(3)
        .map(Path::to_path_buf)
        .context("failed to resolve repository root")
}

fn generate_typescript(roots: &[RootType]) -> anyhow::Result<String> {
    let mut defs = BTreeMap::<String, Value>::new();
    let mut root_schemas = BTreeMap::<String, Value>::new();

    for root in roots {
        let (schema, root_defs) = split_root_schema(root.schema.clone())?;
        root_schemas.insert(root.export_name.to_string(), schema);
        for (name, schema) in root_defs {
            defs.entry(name).or_insert(schema);
        }
    }

    let mut out = String::new();
    out.push_str("// This file is generated by `cargo run --bin generate-schema -p net-meter-control`.\n");
    out.push_str("// Do not edit manually.\n\n");

    let root_names: BTreeSet<_> = root_schemas.keys().cloned().collect();

    for (name, schema) in &root_schemas {
        out.push_str(&render_named_type(name, schema, &defs)?);
        out.push('\n');
    }

    for (name, schema) in &defs {
        if root_names.contains(name) {
            continue;
        }
        out.push_str(&render_named_type(name, schema, &defs)?);
        out.push('\n');
    }

    Ok(out)
}

fn split_root_schema(schema: Schema) -> anyhow::Result<(Value, BTreeMap<String, Value>)> {
    let mut value = schema.to_value();
    let object = value
        .as_object_mut()
        .context("root schema must be an object")?;
    let defs = object
        .remove("$defs")
        .and_then(|value| value.as_object().cloned())
        .unwrap_or_default();
    Ok((value, defs.into_iter().collect()))
}

fn render_named_type(name: &str, schema: &Value, defs: &BTreeMap<String, Value>) -> anyhow::Result<String> {
    let body = match name {
        "TcpPayload" => format!("({} & {{\n  type: \"tcp\";\n}})", render_schema(schema, defs, 0)?),
        "HttpPayload" => format!("({} & {{\n  type: \"http\";\n}})", render_schema(schema, defs, 0)?),
        _ => render_schema(schema, defs, 0)?,
    };
    Ok(format!("export type {name} = {body};\n"))
}

fn render_schema(schema: &Value, defs: &BTreeMap<String, Value>, indent: usize) -> anyhow::Result<String> {
    match schema {
        Value::Bool(true) => Ok("unknown".to_string()),
        Value::Bool(false) => Ok("never".to_string()),
        Value::Object(object) => render_object_schema(object, defs, indent),
        _ => anyhow::bail!("unsupported schema node: {schema}"),
    }
}

fn render_object_schema(
    object: &Map<String, Value>,
    defs: &BTreeMap<String, Value>,
    indent: usize,
) -> anyhow::Result<String> {
    if let Some(reference) = object.get("$ref").and_then(Value::as_str) {
        let reference_type = reference_to_type(reference);
        let mut sibling_schema = object.clone();
        sibling_schema.remove("$ref");
        sibling_schema.remove("title");
        sibling_schema.remove("description");
        sibling_schema.remove("default");
        sibling_schema.remove("$schema");
        sibling_schema.remove("$defs");
        if sibling_schema.is_empty() {
            return Ok(reference_type);
        }
        let sibling_type = render_object_schema(&sibling_schema, defs, indent)?;
        return Ok(format!("({reference_type} & {sibling_type})"));
    }

    if let Some(value) = object.get("const") {
        return Ok(render_literal(value));
    }

    if let Some(values) = object.get("enum").and_then(Value::as_array) {
        let literals = values.iter().map(render_literal).collect::<Vec<_>>();
        return Ok(join_union(&literals));
    }

    if let Some(one_of) = object.get("oneOf").and_then(Value::as_array) {
        let variants = one_of
            .iter()
            .map(|item| render_schema(item, defs, indent))
            .collect::<Result<Vec<_>, _>>()?;
        return Ok(join_union(&variants));
    }

    if let Some(any_of) = object.get("anyOf").and_then(Value::as_array) {
        let variants = any_of
            .iter()
            .map(|item| render_schema(item, defs, indent))
            .collect::<Result<Vec<_>, _>>()?;
        return Ok(join_union(&variants));
    }

    if let Some(all_of) = object.get("allOf").and_then(Value::as_array) {
        let variants = all_of
            .iter()
            .map(|item| render_schema(item, defs, indent))
            .collect::<Result<Vec<_>, _>>()?;
        return Ok(variants.join(" & "));
    }

    if let Some(type_value) = object.get("type") {
        if let Some(type_name) = type_value.as_str() {
            return render_typed_schema(type_name, object, defs, indent);
        }
        if let Some(types) = type_value.as_array() {
            let rendered = types
                .iter()
                .filter_map(Value::as_str)
                .map(|item| render_typed_schema(item, object, defs, indent))
                .collect::<Result<Vec<_>, _>>()?;
            return Ok(join_union(&rendered));
        }
    }

    if object.contains_key("properties") || object.contains_key("additionalProperties") {
        return render_typed_schema("object", object, defs, indent);
    }

    Ok("unknown".to_string())
}

fn render_typed_schema(
    type_name: &str,
    object: &Map<String, Value>,
    defs: &BTreeMap<String, Value>,
    indent: usize,
) -> anyhow::Result<String> {
    match type_name {
        "string" => Ok("string".to_string()),
        "integer" | "number" => Ok("number".to_string()),
        "boolean" => Ok("boolean".to_string()),
        "null" => Ok("null".to_string()),
        "array" => {
            let items = object
                .get("items")
                .map(|item| render_schema(item, defs, indent + 1))
                .transpose()?
                .unwrap_or_else(|| "unknown".to_string());
            Ok(format!("Array<{items}>"))
        }
        "object" => render_ts_object(object, defs, indent),
        _ => Ok("unknown".to_string()),
    }
}

fn render_ts_object(
    object: &Map<String, Value>,
    defs: &BTreeMap<String, Value>,
    indent: usize,
) -> anyhow::Result<String> {
    let required = object
        .get("required")
        .and_then(Value::as_array)
        .map(|items| {
            items.iter()
                .filter_map(Value::as_str)
                .map(ToOwned::to_owned)
                .collect::<BTreeSet<_>>()
        })
        .unwrap_or_default();

    let properties = object
        .get("properties")
        .and_then(Value::as_object)
        .cloned()
        .unwrap_or_default();

    let additional = object.get("additionalProperties");
    let pattern_properties = object
        .get("patternProperties")
        .and_then(Value::as_object)
        .cloned()
        .unwrap_or_default();

    let object_body = if properties.is_empty() {
        None
    } else {
        let mut lines = Vec::new();
        for (name, schema) in properties {
            let optional = if required.contains(&name) { "" } else { "?" };
            let rendered = render_schema(&schema, defs, indent + 1)?;
            lines.push(format!(
                "{}{}{}: {};",
                "  ".repeat(indent + 1),
                render_property_name(&name),
                optional,
                rendered
            ));
        }

        Some(format!(
            "{{\n{}\n{}}}",
            lines.join("\n"),
            "  ".repeat(indent)
        ))
    };

    let additional_body = additional
        .map(|value| -> anyhow::Result<String> {
            match value {
                Value::Bool(true) => Ok("Record<string, unknown>".to_string()),
                Value::Bool(false) => Ok("Record<string, never>".to_string()),
                _ => Ok(format!("Record<string, {}>", render_schema(value, defs, indent + 1)?)),
            }
        })
        .transpose()?;

    let pattern_body = if pattern_properties.is_empty() {
        None
    } else {
        let value_schema = pattern_properties
            .values()
            .next()
            .context("patternProperties must contain at least one schema")?;
        Some(format!("Record<string, {}>", render_schema(value_schema, defs, indent + 1)?))
    };

    let record_body = pattern_body.or(additional_body);

    match (object_body, record_body) {
        (Some(left), Some(right)) => Ok(format!("({left} & {right})")),
        (Some(left), None) => Ok(left),
        (None, Some(right)) => Ok(right),
        (None, None) => Ok("Record<string, never>".to_string()),
    }
}

fn render_property_name(name: &str) -> String {
    if name.chars().all(|ch| ch.is_ascii_alphanumeric() || ch == '_') {
        name.to_string()
    } else {
        serde_json::to_string(name).unwrap_or_else(|_| format!("\"{name}\""))
    }
}

fn render_literal(value: &Value) -> String {
    match value {
        Value::String(value) => serde_json::to_string(value).unwrap_or_else(|_| format!("\"{value}\"")),
        Value::Number(value) => value.to_string(),
        Value::Bool(value) => value.to_string(),
        Value::Null => "null".to_string(),
        _ => "unknown".to_string(),
    }
}

fn join_union(parts: &[String]) -> String {
    if parts.is_empty() {
        "never".to_string()
    } else {
        parts.join(" | ")
    }
}

fn reference_to_type(reference: &str) -> String {
    reference
        .rsplit('/')
        .next()
        .unwrap_or(reference)
        .to_string()
}
